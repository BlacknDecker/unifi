fattorizzazione QR di householder

for i=1:n
	alpha=norm(A(i:m,i));
	if alpha==0
		error(matrice non ha rango massimo);
	end
	if A(i,i)>=0
		alpha = -alpha;
	end
	v1=A(i,i)-alpha
	A(i,i) = alpha;
	A(i+1:m,i) = A(i+1:m,i)/v1;
	beta = -v1/alpha;
	A(i:m,i+1:n) = A(i:m,i+1:n) - (beta*[1; A(i+1:m,i)])*([1 A(i+1:m,i)']*A(i:m,i+1:n));
	% forse dopo l'uno manca un puno e virgola
end
